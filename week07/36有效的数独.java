    public boolean isValidSudoku(char[][] board) {
        Set<Integer>[] row = new Set[9];
        Set<Integer>[] col = new Set[9];
        Set<Integer>[] box = new Set[9];
        for(int i = 0;i<9;i++){
            row[i] = new HashSet<Integer>();
            col[i] = new HashSet<Integer>();
            box[i] = new HashSet<Integer>();
        }
        for(int i = 0;i<9;i++){
            for(int j = 0;j<9;j++){
                if(board[i][j] != '.'){
                    int num = board[i][j] - '1';
                    if(!row[i].add(num)){
                        return false;
                    }
                    if(!col[j].add(num)){
                        return false;
                    }
                    int idx = (i / 3) * 3 + j / 3;
                    if(!box[idx].add(num)){
                        return false;
                    }
                }
            }
        }
        return true;
    }
